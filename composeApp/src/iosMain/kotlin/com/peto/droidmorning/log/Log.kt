package com.peto.droidmorning.log

import platform.Foundation.NSLog

actual object Log {
    actual fun e(
        tag: String,
        message: String,
        throwable: Throwable?,
    ) {
        if (throwable != null) {
            NSLog("ERROR [$tag]: $message. Throwable : $throwable")
        } else {
            NSLog("$tag: $message")
        }
    }

    actual fun d(
        tag: String,
        message: String,
    ) {
        NSLog("DEBUG [$tag]: $message")
    }

    actual fun i(
        tag: String,
        message: String,
    ) {
        NSLog("INFO [$tag]: $message")
    }
}
